/* 5. Реализовать основные арифметические операции: сложение, вычитание, умножение, деление.
Сделать их в виде функций с двумя параметрами.
Обязательно использовать оператор return. */


function plus(a, b) {
  return a + b;
}

function minus(a, b) {
  return a - b;
}

function div(a, b) {
  return a / b;
}

function mult(a, b) {
  return a * b;
}

alert(plus(2, 4));
alert(minus(2, 4));
alert(div(2, 4));
alert(mult(2, 4));